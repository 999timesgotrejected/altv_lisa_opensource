function Distance(vector1, vector2) {
	if (vector1 === undefined || vector2 === undefined) {
		throw new Error('AddVector => vector1 or vector2 is undefined');
	}

	return Math.sqrt(Math.pow(vector1.x - vector2.x, 2) + Math.pow(vector1.y - vector2.y, 2) + Math.pow(vector1.z - vector2.z, 2));
}

export function isPlayerNearAnyFuelPump(player, dist) {
    let isnear = false;
    var i = 0;
    for (i = 0; i < pumps.length; i++) {
        if(Distance(player.pos, pumps[i]) < dist) {
            isnear = true;
            break; 
        }
    }
    return isnear;
}